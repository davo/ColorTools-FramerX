export const getPropsFromColor = (color: string) => {
    const rgb = Color(color)
    let rgbString = Color.toRgbString(rgb)
    rgbString = rgbString.substr(4).slice(0, -1)
    const hex = Color.toHex(rgb)
    const dark = Color.darken(rgb, 60)
    const light = Color.lighten(rgb, 50)
    const hsl = Color.toHsl(rgb)

    const hue = hsl.h.toFixed()
    const saturation = hsl.s.toFixed()
    const lightness = hsl.l.toFixed()

    return {
        rgb,
        rgbString,
        hex,
        dark,
        light,
        hsl,
        hue,
        saturation,
        lightness,
    }
}
